@dataclass
class TrainConfig:
    """All hyperparameters and paths for a training run.

    Pass this to ``train()`` in train.py, and to ``setup_wandb()`` here.
    Using a dataclass (rather than a dict) gives IDE auto-complete and a
    clean ``__repr__`` for free.
    """

    model: str = "yolo11n.pt"
    data_dir: str = "data/raw"
    data_yaml: str = ""           # auto-generated by prepare_dataset() in train.py
    epochs: int = 100
    imgsz: int = 640
    batch: int = 16
    device: str = "0"
    project: str = "runs/train"
    name: str = "hanafuda_yolo11n"
    workers: int = 4
    patience: int = 20
    val_split: float = 0.2        # fraction of data used for validation
    seed: int = 42                # random seed for reproducible train/val split
    wandb_project: str = "hanafuda-yolo"
    wandb_entity: str = field(default="")  # override via env var WANDB_ENTITY

    def as_yolo_kwargs(self) -> dict:
        """Return only the kwargs that ultralytics YOLO.train() accepts."""
        exclude = {"data_dir", "data_yaml", "val_split", "seed", "wandb_project", "wandb_entity"}
        raw = dataclasses.asdict(self)
        raw["data"] = self.data_yaml   # set after prepare_dataset() resolves the path
        return {k: v for k, v in raw.items() if k not in exclude}


