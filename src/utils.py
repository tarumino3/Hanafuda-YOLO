def _class_color(class_id: int) -> tuple[int, int, int]:
    """Deterministic, visually distinct RGB color derived from class index."""
    h = hashlib.md5(str(class_id).encode()).digest()
    r = (h[0] & 0xFF) | 0x40  # clamp to [64, 255] for readability
    g = (h[1] & 0xFF) | 0x40
    b = (h[2] & 0xFF) | 0x40
    return (r, g, b)


def draw_detections(
    image: Image.Image,
    boxes: list[tuple[float, float, float, float]],
    labels: list[int],
    scores: list[float],
    class_names: list[str],
    conf_threshold: float = 0.25,
) -> Image.Image:
    """Draw bounding boxes and labels onto *a copy* of the image.

    Args:
        image: Input PIL image (not mutated).
        boxes: List of (x1, y1, x2, y2) in absolute pixel coordinates.
        labels: Class index for each box.
        scores: Confidence score for each box.
        class_names: Full list of class name strings.
        conf_threshold: Boxes below this confidence are skipped.

    Returns:
        A new PIL Image with detections drawn.
    """
    result = image.copy()
    draw = ImageDraw.Draw(result)

    try:
        font = ImageFont.truetype("arial.ttf", size=18)
    except (IOError, OSError):
        font = ImageFont.load_default()

    for (x1, y1, x2, y2), label, score in zip(boxes, labels, scores):
        if score < conf_threshold:
            continue

        color = _class_color(label)
        draw.rectangle([x1, y1, x2, y2], outline=color, width=3)

        name = class_names[label] if label < len(class_names) else str(label)
        text = f"{name}: {score:.2f}"

        # Filled background rectangle for the text
        bbox = draw.textbbox((x1, y1), text, font=font)
        draw.rectangle(bbox, fill=color)
        draw.text((x1, y1), text, fill="white", font=font)

    return result


