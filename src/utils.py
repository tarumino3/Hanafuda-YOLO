def setup_wandb(config: TrainConfig) -> bool:
    """Initialise a WandB run from *config*.

    Returns True on success, False if WandB is unavailable (e.g. offline
    environment).  Callers should check the return value before calling
    ``wandb.finish()``.
    """
    try:
        import wandb  # noqa: PLC0415
    except ImportError:
        print("[wandb] wandb not installed — skipping WandB logging.")
        return False

    wandb.init(
        project=config.wandb_project,
        entity=config.wandb_entity or None,
        config=dataclasses.asdict(config),
        reinit=True,
    )
    return True
